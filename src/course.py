class Course:
    def __init__(self, course_id , topic, prof_id, major , year , semester , capacity , resources , units):
        self.course_id = course_id
        self.topic = topic
        self.prof_id = prof_id
        self.major = major
        self.year = year
        self.semester = semester
        self.capacity = capacity
        self.resources = resources
        self.units = units